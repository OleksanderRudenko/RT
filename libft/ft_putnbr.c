#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		if (n < -2000000000)
		{
			ft_putnbr(n / (100000));
			ft_putnbr(n % 100000 * (-1));
			return ;
		}
		ft_putchar('-');
		n *= -1;
	}
	if (n / 10 > 0)
		ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0');
}
